  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        elevation: 0,
        title: const Text('Meels'),
      ),
      body: Container(
        padding: const EdgeInsets.all(25),
        child: Categories(),
      ),
    );
  }
}
